     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        //
        $req = $request->all();
        $techsArray = array_map(fn($tech) => trim($tech, " ") ,explode(",", $req['techs']));
        $location = (object) ['latitude' => $req['latitude'], 'longitude' => $req['longitude']];
        $Dev = Dev::whereHas('techs', function ($query) use ($techsArray) {
          return $query->whereIn('name', $techsArray);
        })->closeTo($location, 10)->with('techs'); //Usa o mÃ©todo pesquisando por 10km
        return $Dev->get();
    }

